// --- HELPERS ---

function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function pickWithRoll(arr, roll = Math.random()) {
  const normalized = Math.max(0, Math.min(0.999999, roll));
  return arr[Math.floor(normalized * arr.length)];
}

function getQuipTone(mode = {}) {
  if (mode.endless) return 'endless';
  if ((mode.iskander?.interval?.[0] || 9999) < 600) return 'harsh';
  if ((mode.waveBonus || 0) >= 55 || (mode.turret?.hitChance || 0) > 0.5) return 'training';
  return 'default';
}

function getTier(amount) {
  if (amount >= 45) return 'large';
  if (amount >= 25) return 'medium';
  return 'small';
}

export function getKillQuip(enemyType) {
  const quips = KILL_QUIPS[enemyType];
  if (!quips) return null;
  // 15% chance to show a quip per kill
  if (!chance(0.15)) return null;
  // 5% chance for a rare/humor quip instead
  if (chance(0.05)) return pick(RARE_QUIPS);
  return pick(quips);
}

export function getWaveStartQuip() {
  return pick(WAVE_START);
}

export function getWaveCompleteQuip() {
  return pick(WAVE_COMPLETE);
}

export function getBattleCalloutText(kind, mode = null, roll = Math.random()) {
  const mood = getQuipTone(mode || {});
  const pool = BATTLE_CALLOUTS[kind]?.[mood] || BATTLE_CALLOUTS[kind]?.default;
  if (!pool?.length) return null;
  return pickWithRoll(pool, roll);
}

export function getWaveFundingQuip({ amount, net = amount, mode = null }, roll = Math.random()) {
  const mood = getQuipTone(mode || {});
  const tier = getTier(amount);
  const pool = FUNDING_QUIPS[mood]?.[tier] || FUNDING_QUIPS.default[tier];
  const prefix = pickWithRoll(pool, roll);
  const tail = net <= 0 ? ' Ледве перекрили витрати.' : '';
  return `${prefix} +${amount}💰${tail}`;
}

export function getUpkeepQuip({ amount, net = 0, mode = null }, roll = Math.random()) {
  const mood = getQuipTone(mode || {});
  const tier = amount >= 25 ? 'large' : 'small';
  const pool = UPKEEP_QUIPS[mood]?.[tier] || UPKEEP_QUIPS.default[tier];
  const prefix = pickWithRoll(pool, roll);
  const tail = net <= 0 ? ' На руках після цього сумно.' : '';
  return `${prefix} -${amount}💰${tail}`;
}

