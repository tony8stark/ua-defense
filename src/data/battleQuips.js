// --- HELPERS ---

function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function getKillQuip(enemyType) {
  const quips = KILL_QUIPS[enemyType];
  if (!quips) return null;
  // 15% chance to show a quip per kill
  if (!chance(0.15)) return null;
  // 5% chance for a rare/humor quip instead
  if (chance(0.05)) return pick(RARE_QUIPS);
  return pick(quips);
}

export function getWaveStartQuip() {
  return pick(WAVE_START);
}

export function getWaveCompleteQuip() {
  return pick(WAVE_COMPLETE);
}

export function getIskanderQuip(event) {
  if (event === 'incoming') return pick(ISKANDER_INCOMING);
  if (event === 'hit') return pick(ISKANDER_HIT);
  if (event === 'scramble') return pick(ISKANDER_SCRAMBLE);
  return null;
}

export function getF16Quip(event) {
  if (event === 'arrive') return pick(F16_ARRIVE);
  if (event === 'missile') return pick(F16_MISSILE);
  return null;
}

export function getEWQuip(event) {
  if (event === 'start') return pick(EW_START);
  if (event === 'end') return pick(EW_END);
  return null;
}

export function getWeatherQuip(weatherId) {
  const quips = WEATHER_QUIPS[weatherId];
  if (!quips) return null;
  return pick(quips);
}

export function getIntelQuip() {
  return pick(INTEL_BETWEEN_WAVES);
}
